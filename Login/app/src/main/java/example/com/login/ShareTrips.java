package example.com.login;

public class ShareTrips {


    String id ;
    String user;
    String preference;
    String userid;
    String budget;
    String meal_pref;
    String meal_comment;
    String type;
    String date;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getPreference() {
        return preference;
    }

    public void setPreference(String preference) {
        this.preference = preference;
    }

    public String getUserid() {
        return userid;
    }

    public void setUserid(String userid) {
        this.userid = userid;
    }

    public String getBudget() {
        return budget;
    }

    public void setBudget(String budget) {
        this.budget = budget;
    }

    public String getMeal_pref() {
        return meal_pref;
    }

    public void setMeal_pref(String meal_pref) {
        this.meal_pref = meal_pref;
    }

    public String getMeal_comment() {
        return meal_comment;
    }

    public void setMeal_comment(String meal_comment) {
        this.meal_comment = meal_comment;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
